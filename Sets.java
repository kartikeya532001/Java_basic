package javab;

import java.util.LinkedHashSet;
import java.util.Set;

public class Sets {
	public static void main(String[] args) {
		Set<String> set = new LinkedHashSet<String>();
		set.add("S");
		set.add("A");
		set.add("U");
		set.add("R");
		set.add("A.");
		set.add("V");

		System.out.println(set);
	}

}
